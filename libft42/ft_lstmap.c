#include "libft.h"

t_list	*ft_lstmap(t_list *lst, t_list *(*f)(t_list *elem))
{
	t_list *new;

	if (!lst || !f)
		return (NULL);
	new = malloc(sizeof(t_list));
	new = f(lst);
	new->next = ft_lstmap(lst->next, f);
	return (new);
}
