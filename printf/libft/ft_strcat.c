#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	const char	*copy;
	size_t		i;
	size_t		j;

	copy = (const char*)s1;
	j = 0;
	i = ft_strlen(copy);
	while (s2[j] != '\0')
	{
		s1[i] = s2[j];
		i++;
		j++;
	}
	s1[i] = '\0';
	return (s1);
}
